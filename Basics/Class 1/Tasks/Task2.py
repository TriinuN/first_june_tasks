# This algebra monster, equation to solve a3 – b3, formula: (a – b)(a2 + ab + b2)
print("Let's finish this algebra monster a3 – b3!!")

a= int(input("Please enter number a"))
b= int(input("Please enter number b"))

Formula = (a - b) * (a**2 + a * b + b**2)

print("The result of this equation is: ", Formula)

